namespace Docs\Data;

use App\Util\Icons;
use Spatie\LaravelData\Data;

class Language extends Data
{
    public function __construct(
        public string $name,
        public string $icon,// svg
        // public string $path,
    ) {}

    public function __toString()
    {
        return $this->name;
    }

    public static function fromString(string $language)
    {
        return new static(
            name: $language,
            icon: Icons::get($language),
        );
    }
}
